public class SspNode {

		int edge;
		int weight;
		SspNode id =  null;
		
	
		
		public int getEdge() {
			return edge;
		}
		public void setEdge(int edge) {
			this.edge = edge;
		}
		public int getWeight() {
			return weight;
		}
		public void setWeight(int weight) {
			this.weight = weight;
		}
		public SspNode getId() {
			return id;
		}
		public void setId(SspNode id) {
			this.id = id;
		}
		
		
}
